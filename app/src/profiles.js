// Fun√ß√£o para embaralhar um array
function shuffleArray(array) {
  for (let i = array.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [array[i], array[j]] = [array[j], array[i]];
  }
  return array;
}

// Fun√ß√£o para exibir um perfil espec√≠fico em um determinado elemento
function exibirPerfil(perfil, containerId) {
  const containerPerfis = document.getElementById(containerId);
  containerPerfis.innerHTML = ""; // Limpar o conte√∫do anterior

  const perfilDiv = document.createElement("div");
  perfilDiv.classList.add("col-lg-6", "offset-lg-3", "boxl1");

  const lookDiv = document.createElement("div");
  lookDiv.id = "looks";
  lookDiv.classList.add("look");

  const perfilInfoDiv = document.createElement("div");
  perfilInfoDiv.classList.add("perfil");

  const postHeaderDiv = document.createElement("div");
  postHeaderDiv.classList.add("postheader");

  const perfilInfoDivInner = document.createElement("div");
  perfilInfoDivInner.classList.add("perfil-info");

  const fotoPerfilDiv = document.createElement("div");
  fotoPerfilDiv.classList.add("foto-perfil");
  fotoPerfilDiv.innerHTML = `<img src="${perfil.fotoPerfil}" alt="${perfil.nome}" />`;

  const nomePerfilH1 = document.createElement("h1");
  nomePerfilH1.classList.add("nome-perfil");
  nomePerfilH1.textContent = perfil.nome;

  const imgDots = document.createElement("img");
  imgDots.src = "./assets/img/dots.svg";
  imgDots.width = "16";

  const feedDiv = document.createElement("div");
  feedDiv.classList.add("feed");

  const nome2Div = document.createElement("div");
  nome2Div.style.paddingLeft = "10px";
  nome2Div.style.paddingBottom = "12px";
  nome2Div.style.paddingRight = "10px";
  nome2Div.innerHTML = `<span class="nome2" style="font-weight: bold;">${perfil.nome}</span><span id="legenda">...</span>`;

  postHeaderDiv.appendChild(perfilInfoDivInner);
  postHeaderDiv.appendChild(imgDots);
  perfilInfoDivInner.appendChild(fotoPerfilDiv);
  perfilInfoDivInner.appendChild(nomePerfilH1);
  perfilInfoDiv.appendChild(postHeaderDiv);
  perfilDiv.appendChild(lookDiv);
  lookDiv.appendChild(perfilInfoDiv);
  lookDiv.appendChild(feedDiv);
  lookDiv.appendChild(nome2Div);
  containerPerfis.appendChild(perfilDiv);

  // Selecionar uma postagem aleat√≥ria do perfil e exibi-la no feed
  const randomIndex = Math.floor(Math.random() * perfil.postagens.length);
  const postagem = document.createElement("div");
  postagem.classList.add("postagem");
  const imagem = document.createElement("img");
  const altText = perfil.postagens[randomIndex].alt; // Armazenar o texto alt
  imagem.src = perfil.postagens[randomIndex].imagem; // Acessar a propriedade imagem
  imagem.alt = altText; // Definir o atributo alt
  postagem.appendChild(imagem);
  feedDiv.appendChild(postagem);

  // Inserir o texto alt dentro do elemento span com o ID "legenda"
  const legenda = nome2Div.querySelector("#legenda");
  legenda.textContent = altText;
}

